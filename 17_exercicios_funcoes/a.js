console.clear()


let numero1 = 8;
let numero2 = 5;

function adicao(n1, n2) {
    return n1 + n2
}

function subtrair(n1, n2){
    return n1 - n2
}

function multiplicar(n1 , n2){
    return n1 * n2
}

function dividir(n1 , n2){
    return n1 / n2
}

let soma = adicao(numero1 , numero2)
let subtracao = subtrair(numero1 , numero2)
let multiplicacao = multiplicar(numero1 , numero2)
let divisao = dividir(numero1 , numero2)

console.log('a soma é '+ soma)
console.log('a subtracao é '+ subtracao)
console.log('a multiplicacao é '+ multiplicacao)
console.log('a divisao é ' + divisao)


