// como fazer uma iteração(ciclo, laço) em um array
console.clear()

let nomes = ['jo maia', 'diana', 'vaneilza', 'pamela', 'cristina', 'luciana', 'bruna'];

// com o ciclo foreach como metodo do array, podemos fazer uma iteração e executar uma função
// para cada um dos elementos da coleção

nomes.forEach(function (e) {
    console.log(e)
})

